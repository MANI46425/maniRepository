package corejava;

public class A_to_Z 
{
	
	    public static void main(String[] args) {
	        char character = 'a';
	        while (character <= 'z') {
	            if (character >= 'a' && character <= 'z')
	            {
	                System.out.print(character + " ");
	            }
	            character++;
	        }
	    }
	}


